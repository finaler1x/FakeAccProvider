class FakeMailAddress:
    def __init__(self, address, cookies, inbox):
        self.address = address,
        self.cookies = cookies,
        self.inbox   = inbox,

def getAddress(self):
    return self.address

def setAddress(self, address):
    self.address = address

def getCookies(self):
    return self.cookies

def setCookies(self, cookies):
    self.cookies = cookies

def getInbox(self):
    return self.inbox

def setInbox(self, inbox):
    self.inbox = inbox

